// Detect best language from browser
function detectLang() {
  const nav = navigator.language || navigator.languages?.[0] || 'en';
  const code = nav.split('-')[0].toLowerCase();
  return LANGS[code] ? code : 'en';
}

let _currentLang = 'en';

async function initI18n() {
  const store = await chrome.storage.local.get(['lang']);
  _currentLang = store.lang || detectLang();
  applyI18n();
}

function setLang(lang) {
  _currentLang = lang;
  chrome.storage.local.set({ lang });
  applyI18n();
}

function getLang() {
  return _currentLang;
}

function t(key) {
  return MESSAGES[_currentLang]?.[key] || MESSAGES.en[key] || key;
}

// Scan DOM for data-i18n and data-i18n-title attributes and apply translations
function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.dataset.tooltip = t(el.dataset.i18nTitle);
  });
}
